package com.usc.softandroid.model.vo;

public class Dimension {
	private int height, width;
	
	public Dimension(){
		
	}
	
	public void setHeight(int h){
		height = h;
	}
	
	public void setWidth(int w){
		width = w;
	}
	
	public int getHeight(){
		return height;
	}
	
	public int getWidth(){
		return width;
	}
}
